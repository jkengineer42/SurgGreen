# ── FONCTIONS DE SCORING ───────────────────────────────────────────────────────

def score_clinique(m: dict) -> float:
    s = (
        (m["taux_succes_pct"] / 100) * 3.5
        + (m["biocompatibilite"] / 5)  * 2.5
        + (m["osteointegration"] / 5)  * 2.0
        - (m["risque_infection"] / 5)  * 1.0
        - (m["risque_allergie"]  / 5)  * 1.0
    )
    return round(min(max(s, 0.0), 10.0), 2)


def score_environnemental(m: dict) -> float:
    co2 = m["co2_kg_par_kg"]
    if co2 <= 2:
        base = 9.5
    elif co2 <= 5:
        base = 9.0 - ((co2 - 2) / 3) * 2.0
    elif co2 <= 15:
        base = 7.0 - ((co2 - 5) / 10) * 3.0
    elif co2 <= 50:
        base = 4.0 - ((co2 - 15) / 35) * 3.0
    else:
        base = max(0.3, 1.0 - ((co2 - 50) / 150))

    if m["biodegradable"]:  base += 1.5
    if m.get("recyclable"): base += 0.5
    return round(min(base, 10.0), 2)


def score_stress_shielding(m: dict) -> float:
    gpa = m.get("module_elastique_gpa", 100.0)
    ratio = abs(gpa - 20.0) / 20.0
    s = max(0.0, 10.0 - ratio * 2.0)
    return round(min(s, 10.0), 2)


def score_global(m: dict) -> float:
    return round(
        score_clinique(m)               * 0.40
        + score_environnemental(m)      * 0.25
        + (m["taux_succes_pct"] / 10)   * 0.20
        + (m["disponibilite"] / 5 * 10) * 0.10
        + score_stress_shielding(m)     * 0.05,
        2,
    )
