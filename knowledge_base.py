def get_cout_transport(key: str, poids_kg: float = 0.1) -> dict:
    """
    Calcule le coût carbone et financier du transport pour un matériau donné.
    poids_kg : poids estimé de l'implant (défaut 100g = 0.1 kg)
    Retourne un dict avec co2_transport_kg, cout_transport_eur, distance_km, mode.
    """
    orig = ORIGINES.get(key, {})
    if not orig:
        return {"co2_transport_kg": 0, "cout_transport_eur": 0, "distance_km": 0, "mode": "—"}

    dist = orig["distance_km"]
    mode = orig["mode_transport"]
    facteurs = TRANSPORT_FACTEURS[mode]

    co2  = round(dist * poids_kg * facteurs["co2"], 4)
    cout = round(dist * poids_kg * facteurs["cout"], 4)

    return {
        "co2_transport_kg":  co2,
        "cout_transport_eur": cout,
        "distance_km": dist,
        "mode": mode,
    }


